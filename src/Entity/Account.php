<?php

namespace App\Entity;

use Doctrine\ORM\Mapping as ORM;
use Symfony\Component\Security\Core\User\UserInterface;

/**
 * @ORM\Entity
 */
class Account implements UserInterface
{
    /**
     * @ORM\Id
     * @ORM\GeneratedValue
     * @ORM\Column(type="integer")
     */
    private $id;

    /**
     * @ORM\Column(type="string", unique=true)
     */
    private $username;

    /**
     * @ORM\Column(type="string")
     */
    private $password;

    /**
     * @ORM\Column(type="json")
     */
    private $roles = [];

    // Constructor, getters, and setters

    public function __construct()
    {
        // Initialize any necessary properties or relationships
    }

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getUsername(): ?string
    {
        return $this->username;
    }

    public function setUsername(string $username): void
    {
        $this->username = $username;
    }

    public function getPassword(): ?string
    {
        return $this->password;
    }

    public function setPassword(string $password): void
    {
        $this->password = $password;
    }

    public function getRoles(): array
    {
        return $this->roles;
    }

    public function setRoles(array $roles): void
    {
        $this->roles = $roles;
    }

    // Other methods required by UserInterface

    public function getSalt(): ?string
    {
        // This method is not needed if you are using bcrypt or a modern hashing algorithm
        return null;
    }

    public function getUserIdentifier(): string
    {
        // Provide the implementation here
        return $this->username;
    }

    public function eraseCredentials(): void
    {
        // This method is not needed if you don't store plain-text passwords or sensitive information
    }
}
